package operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		int a = 15;
		int b = 18;
		
		System.out.println(a);
		
		 a += 2; // same as a = a+2
		 
		 //others may be a*=2, a-=2, a/=, a%=2
		 // a%=2 may be used to determine if number is even or odd
		 
		 
		 
		 b = b + 2;
		
		System.out.println(a);
		
		System.out.println(b);

	}

}
