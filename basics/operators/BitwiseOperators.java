package operators;

public class BitwiseOperators {

	public static void main(String[] args) {
		int a = 4;
		
		int b = 5;
		
		int c = a & b;
		
		int d = 15;
		
		int e = d>>1;
		
		int f = d>>3;
		
		int g = d<<1;
		
		System.out.println(c);
		
		System.out.println(e);
		
		System.out.println(f);
		
		System.out.println(g);
		
		// & = and , | = or , >> = right shift, << = left shift

	}

}
